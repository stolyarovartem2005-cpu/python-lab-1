def read_int(prompt: str) -> int:
    """Зчитує ціле число з клавіатури з перевіркою коректності."""
    while True:
        s = input(prompt).strip()
        try:
            return int(s)
        except ValueError:
            print("Помилка: потрібно ввести ціле число (наприклад: 10). Спробуйте ще раз.")


def read_float(prompt: str) -> float:
    """Зчитує дробове число з клавіатури з перевіркою коректності."""
    while True:
        s = input(prompt).strip().replace(",", ".")  # на випадок, якщо введете кому
        try:
            return float(s)
        except ValueError:
            print("Помилка: потрібно ввести число (наприклад: 2.5). Спробуйте ще раз.")


